package org.jolly_handball.sps_hc20.chrono;

public class Figures {
    private int left;
    private int right;

    Figures(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }
}
